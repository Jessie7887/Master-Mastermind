# Error messages for invalid entries >>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>
def char_error():
    message = "Error. Entry must be a character."
    print(message)


def color_error():
    message = "Error. Entry must be a valid color."
    print(message)


def repeat_error():
    message = "Error. Each character of your entry must be unique. No repeats!"
    print(message)


def length_error():
    message = "Error. Length of entry must be 4."
    print(message)


